        <h1>UNM Honors College</h1>
<p>The Honors College provides the opportunity for high achieving students to develop a broad-based and flexible interdisciplinary liberal arts education similar to that of many small liberal arts colleges. By choosing either a major, minor or Transcripted Designation in the Honors College, students will have the opportunity to discover connections among disciplines, analyze and evaluate primary and complex texts across diverse genres and styles and from different historical periods. They will be expected to complete courses from a broad range of disciplines that will prepare them for careers or advanced study beyond their undergraduate degree. The B.A. in Honors Interdisciplinary Liberal Arts will provide students with a foundation in social and behavioral studies, physical and natural sciences, humanities, communications, mathematics, and fine arts and allow students to focus on a specific area of interdisciplinary study. Students will engage in research and produce original work that integrates ideas and methods from different disciplines and learn to adapt to new environments and developing technologies. In addition, Honors College students will be expected to have intercultural knowledge and competence and develop personal and social responsibility, including civic knowledge and engagement&mdash;local and global. These expectations form the major learning outcomes and will be the basis for program assessment.</p>

