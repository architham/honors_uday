        <h1>Honor College Designation</h1>


<p>
  The Honors College Designation is to be awarded to high-achieving students who do not earn a major or minor in the Honors College, but who gain an Honors experience by completing a program of Honors Coursework. Students who complete the Designation will be expected to produce work that integrates ideas and methods from different disciplines, to analyze and evaluate foundational and primary works, to gain knowledge of diverse cultures and to acquire civic knowledge and apply ethical reasoning. These expectations form the designation&rsquo;s learning outcomes and will be the basis for program assessment.</p>

