<p style="margin-left:81pt;">
  &sect;&nbsp; The successful completion of 15 credit hours in honors courses</p>
<p style="margin-left:81pt;">
  A minimum of 3 credit hours in HC 100 level</p>
<p style="margin-left:81pt;">
  A minimum of 3 credit hours in HC 200 level</p>
<p style="margin-left:81pt;">
