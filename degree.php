        <h1>UNM Honors College</h1>
<p>
  The faculty of the University of New Mexico offers the degree of Interdisciplinary Bachelor of Arts to high achieving students in the Honors College. This baccalaureate degree program provides the opportunity for students in the Honors College to develop a broad-based and flexible interdisciplinary liberal arts education similar to what many small liberal arts colleges offer. The BA in Honors Interdisciplinary Liberal Arts will provide students with a foundation in social and behavioral studies, physical and natural sciences, humanities, communications, mathematics, and fine arts and allow students to focus on a specific area of interdisciplinary study.</p>

<p>
  Students majoring in the Honors College will have the opportunity to discover connections among disciplines analyze and evaluate primary and complex texts across diverse genres and styles and from different historical periods. They will do research and produce original work that integrates ideas and methods from different disciplines and learn to adapt to new environments and developing technologies. Students will be expected to have intercultural knowledge and competence and develop personal and social responsibility, including civic knowledge and engagement-local and global.</p>



