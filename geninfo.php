        <h1>UNM Honors College General Information</h1>
<p>
  The Honors College provides the opportunity for high achieving students to develop a broad-based and flexible interdisciplinary liberal arts education similar to that of many small liberal arts colleges. By choosing either a major, minor or Transcripted Designation in the Honors College, students will have the opportunity to discover connections among disciplines, analyze and evaluate primary and complex texts across diverse genres and styles and from different historical periods. They will be expected to complete courses from a broad range of disciplines that will prepare them for careers or advanced study beyond their undergraduate degree. The B.A. in Honors Interdisciplinary Liberal Arts will provide students with a foundation in social and behavioral studies, physical and natural sciences, humanities, communications, mathematics, and fine arts and allow students to focus on a specific area of interdisciplinary study.</p>
<p>
  Students will engage in research and produce original work that integrates ideas and methods from different disciplines and learn to adapt to new environments and developing technologies. In addition, Honors College students will be expected to have intercultural knowledge and competence and develop personal and social responsibility, including civic knowledge and engagement&mdash;local and global. These expectations form the major learning outcomes and will be the basis for program assessment.</p>


  <h3><u>Admission Requirements</u></h3>
<p>
  The following are the minimum requirements for admission to the Honors College:</p>
<ol>
  <li>
    A high school cumulative G.P.A. of 3.5 or higher and an ACT cumulative test score of 29 (or higher) or SAT score of 1860 (or higher).</li>
  <li>
    Transfer or Current UNM students must have a cumulative G.P.A. of 3.20 or higher.</li>
  <li>
    High School applicants with scores at or below a 3.4 G.P.A./ 28 ACT or 1859 SAT, and college applicants with a G.P.A. at or below 3.1 must also include a 1-page personal essay. The 1-page essay should be about you, your goals, and your interest in the Honors College. Special consideration is given to essays which demonstrate community/academic involvement and h3 written communication.</li>
  <li>
    All students are required to submit an application for admission to the Honors College. Application deadlines are:</li>
</ol>
<p>
  &middot; Start in Fall Semester: February 12 for early decision and May 1 final deadline.</p>
<p>
  Start in Spring Semester: <strong>Deadline is November 1.</strong></p>

  <h2>Degrees Offered</h2>

  <h3><u>Honors Designation in Interdisciplinary Studies</u></h3>
<p>
  The Honors College Designation is to be awarded to high-achieving students who do not choose to earn a major or minor in the Honors College, but who wish to gain an Honors experience by completing a program of Honors coursework. Students who complete the Designation will be expected to produce work that integrates ideas and methods from different disciplines, to analyze and evaluate foundational and primary works, to gain knowledge of diverse cultures and to acquire civic knowledge and apply ethical reasoning. These expectations form the Designation&rsquo;s learning outcomes and will be the basis for program assessment.</p>
<p>
  <h3>Requirements</h3></p>
<ol>
  <li>
    Admission to the Honors College.</li>
  <li>
    Maintenance of a 3.20 G.P.A.</li>
  <li>
    The successful completion of 15 credit hours in HON courses or approved disciplinary Honors courses, with a minimum of 9 credit hours completed in HON courses.</li>
  <li>
    Introductory Course: A minimum of 3 credit hours of HON 121 or 122.</li>
  <li>
    Lower Division Courses: A minimum of 3 credit hours in 200-level courses.</li>
  <li>
    Upper Division Courses: A minimum of 6 credit hours in 300/400-level courses.</li>
</ol>

  <h3><u>Minor in Honors Interdisciplinary Studies</u></h3>
<p>
  The Honors College Interdisciplinary Studies Minor is intended to complement, broaden and enhance a student&#39;s educational choices while at UNM. Academic standards for the minor are rigorous. Students who complete a Minor in Honors Interdisciplinary Studies will be expected to produce original work that integrates ideas and methods from different disciplines, to analyze and evaluate foundational and primary works, to gain knowledge of diverse cultures and to acquire civic knowledge and apply ethical reasoning. These expectations form the minor&rsquo;s learning outcomes and will be the basis for program assessment.</p>

  <h3>Requirements</h3>
<ol>
  <li>
    Admission to the Honors College.</li>
  <li>
    Maintenance of 3.20 G.P.A.</li>
  <li>
    The successful completion of 24 credit hours.</li>
  <li>
    Introductory Course: A minimum of 3 credit hours of HON 121 or 122.</li>
  <li>
    Lower Division Courses: A minimum of 3 credit hours in 200-level HON courses or Honors core curriculum courses (numbers not yet assigned).</li>
  <li>
    Upper Division Courses: A minimum of 12 credit hours in HON courses at or above the 300 level.</li>
  <li>
    Experiential Courses: A minimum of 6 credit hours in HON experiential courses, as approved by the Honors College Curriculum Committee. International experiential programs and courses are preferred, but not required.</li>
</ol>

<p>
  <h3><u>Bachelor of Arts in Honors Interdisciplinary Liberal Arts</u></h3></p>
<p>
  The Bachelor of Arts in Honors Interdisciplinary Liberal Arts will provide students with a rigorous foundation in social and behavioral studies, physical and natural sciences, humanities, communications, mathematics, and fine arts and allow students to focus on a specific area of interdisciplinary study. The Honors Interdisciplinary Liberal Arts Major requires students to fulfill the degree requirements for the college as well as 36 credit hours of major requirements. Students pursuing this degree will work closely with the Honors College to develop a program of study and must receive approval from the Honors College Degree Committee for all course work in fulfillment of the major.</p>
<p>
  <h3>Requirements</h3></p>
<p>
  Students earning minors and transcripted designations in the Honors College are required to fulfill the requirements expected of the college of their major. A degree awarded by the Honors College is granted upon completion of the following requirements :</p>
