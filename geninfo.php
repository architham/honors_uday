  <li>
    Completion of all UNM core requirements, some of which can be completed through Honors College courses.</li>
  <li>
    The ability to communicate in a language other than English is becoming imperative to participate and lead in today&rsquo;s global marketplace. Honors College majors must complete 12 credit hours of a single non-English language or provide an equivalent proficiency document &lt;<a href="http://www.unm.edu/~fll">www.unm.edu/~fll</a>&gt; or &lt;<a href="http://www.unm.edu/spanport">www.unm.edu/spanport</a>&gt;.</li>
  <li>
    A student must choose a minor or a second major from a field of study that complements or enhances a student&rsquo;s area of research interest. Must be approved by Honors College Degree Committee.</li>
  <li>
    Completion of 18 hours of upper division courses (300-400 levels) approved by the Associate Dean in courses that will enrich a student&rsquo;s knowledge in the following areas:</li>
</ol>
