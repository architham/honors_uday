<p class="modified">
  To apply to the University of New Mexico Honors College, incoming freshmen, current UNM students or transfer students must fill out an <a href="https://spreadsheets.google.com/viewform?formkey=dG9leF9zYWhlOFd1MmZ3UGZKU3NqRUE6MQ">application</a> and submit unofficial transcripts. <em>Transfer or current UNM students should ideally apply to the Honors College by the end of their freshmen year.</em></p>
<h3>
  Requirements of the Honors College</h3>
<p>
  Incoming Freshmen Applicants (High School Seniors): Unofficial high school transcript showing a cumulative GPA of 3.5 or higher; and ACT cumulative test score of 29 (or higher) or SAT score of 1860 (or higher);</p>
<p>
  Transfer or current UNM Student Applicants: Unofficial college/university transcript showing a cumulative GPA of 3.20 or higher; and have completed English 101 or its equivalent</p>
<p>
  <em>High School applicants with scores at or below a 3.4 GPA/28 ACT/1859 SAT and college applicants with a GPA at or below 3.10 must also include a 1-page personal essay.</em>&nbsp;&nbsp;The 1-page essay should be about you, your goals, and your interest in the Honors College. Special consideration is given to essays which demonstrate community/academic involvement and strong written communication.</p>
<h3>
  Deadlines</h3>
<p>
  When to apply depends largely on when students will start at UNM or will start taking Honors courses. The Honors College main office will send notifications of acceptance soon after these deadlines.</p>
<p>
  <em>Start in a Fall Semester</em><em>: &nbsp;&nbsp;February 12 for best consideration and May 1 final deadline </em></p>
<p>
  <em>Start in a Spring Semester: &nbsp;</em><em>November 1</em>: All applicants</p>
<p>
  <em>Whenever deadlines fall on a weekend or a holiday, materials are due by the following Monday. </em></p>
<h3>
  Deferring Applications</h3>
<p>
  Students with scores below the minimums are encouraged to defer their applications for one semester. As an entering freshman, wait one semester, get a feel for the UNM course loads, and apply in your second semester.</p>

