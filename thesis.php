        <h1>UNM Honors College | Thesis or Project</h1>

        <h3>Senior Thesis/Creative Project</h3>

<p>Students majoring in Honors must complete a capstone experience in the Honors College.  The capstone experience consists of a senior honors interdisciplinary thesis/creative project which allows you to work in-depth, one-on-one with a professor of your choice on a topic that’s meaningful to you. It is a two-semester project/thesis.
</p>
  
<p>This individualized final requirement, normally completed in the seventh or eighth semester, allows you to integrate your own undergraduate experience. The Honors College does not restrict the field or endeavor that you may work on for your senior honors project or thesis. When you approach your senior year, it’s likely that you’ll be ready, even eager, to undertake a rigorous project on a topic or in a field that you have chosen. You might do this by investigating a significant topic and developing a project that demonstrates the results and implications of your research.</p>

<p>
The senior project frequently involves a written thesis, but many varieties are possible. You may wish to compose a group of short stories, mount an exhibition, produce and direct a video, or plan a recital and perform in it. </p>

<p>
Before you can register for your honors senior project or thesis (HON 490 and 491), you must review your plans with a professor, who will serve as your thesis/project advisor and the dean or associate dean and submit a signed, approved thesis proposal. </p>
<p>
Whatever type of project you choose, keep in mind that this capstone experience represents final proof of your worthiness to be a UNM Honors College graduate. The project also frequently becomes the centerpiece for your application for awards, scholarships, or graduate or professional study.
</p>
<h4>Project Guides</h4>
<p>This abbreviated Web guide introduces and explains the requirements of the senior honors thesis/creative project and provides a list of common questions and answers. It also indicates the range of possibilities open to you by offering examples of past theses and projects completed by recent honors students.</p>


  
