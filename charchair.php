        <h1>Carruthers Chair</h1>

<p>The UNM Honors College invites nominations and applications for appointments 
to the Garrey Carruthers Distinguished Chair in Honors. The Carruthers 
Chair has been endowed by the Burlington Resources Foundation. 
Appointments to the Visiting Distinguished professorship will be made for 
periods of one or two semesters.</p>
<p>Appointees to the Carruthers Chair will be selected on the basis of their 
experience and interest in academic interdisciplinary activities, outstanding 
credentials in undergraduate teaching, experience in Honors education, 
nationally/internationally acclaimed scholarship and/or creative endeavors, and 
awards and prizes. <strong>Junior faculty need not apply.</strong> 
It is expected that the holders of the Chair will teach two undergraduate 
courses per semester in the UNM Honors College designed to their specifications, 
interact with faculty and undergraduates in a number of informal and formal 
settings, and present one or more public lectures.</p>
<p>UNM’s UNM Honors College offers stimulating courses with an emphasis on 
liberal education in small interdisciplinary courses. The courses enhance 
analytical thinking and encourage interchange of ideas among students from a 
broad diversity of disciplines. Located in Albuquerque, New Mexico, the 
University is a Carnegie Research I Institution and DoEd Minority Public 
university with an enrollment of 30,000 undergraduate, graduate and professional 
students on five campuses. In addition to its own resources, the 
university offers ideal access to the scientific, artistic, cultural and natural 
riches of Santa Fe, Taos, Los Alamos, and other New Mexico areas.</p>
